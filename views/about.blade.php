<h1>Objective</h1>

<p>The purpose of this assignment is to apply what you learnt about the client side and server- side scripting to create an expense tracker web application.
Scenario
</p>
<p>
An expense tracker can track any expenses that involve the exchange of money, whether it is cash or electronic payment. It provides a convenient way to record and categorize expenses, allowing users to monitor their spending patterns, set budgets, and gain insights into their financial habits.
Consider the above scenario and develop a web application by applying the web engineering concepts on the following features:
</p>
<p>
•	Categorization: An expense tracker can categorize expenses into different categories,
</p>
<p>
such as food, housing, transportation, entertainment, and more.
</p>
<p>
•	Payment type: An expense tracker can track expenses made with various payment methods, including cash, credit/debit cards.
</p>
<p>
•	Frequency: An expense tracker can track expenses that occur daily, weekly or monthly.
</p>
<p>
•	Reporting: An expense tracker can generate reports that summarize spending by category, payment method, or time period.
</p>
<p>
•	Analyze and apply the best security solution to develop this tracker.
Task
</p>
<p>
Develop an Expense Tracker web application while considering the above features by applying the appropriate PHP concepts of client-side and server-side scripting.
</p>
